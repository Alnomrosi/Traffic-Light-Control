#ifndef TYPEDEFS_H_
#define TYPEDEFS_H_

/*
char
*/
typedef unsigned char 	u8;		// USED
typedef signed char 	s8;

/*
int
*/
typedef unsigned short int u16;		// 2 bytes = 16 bits  USED 
typedef signed short int   s16;		// 2Byte

typedef unsigned long int  u32; // 4byte
typedef signed long  int   s32;

/*
float
*/
typedef float 		f32 ;
typedef double 		f64 ;
typedef long double f128;




#endif /* TYPEDEFS_H_ */